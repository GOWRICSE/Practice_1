package practice_2;

public class Wrapper_class {

	public static void main(String[] args)
	{
		char ch='a';
		Character objch=ch;
		System.out.println("character as primitive : "+ch);
		System.out.println("object character : "+objch);
		int a=5;
		Integer objint=a;
		System.out.println("integer as primitive : "+a+"\nobject integer : "+objint);
		float fl=5.2f;
		Float objfl=fl;
		System.out.println("float as primitive : "+fl+"\nobject float : "+objfl);
		

	}

}